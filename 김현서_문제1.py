# 계단 오르는 방법의 수 계산 프로그램


# 1. 사용자로부터 계단 수 입력받기
n = int(input("계단의 개수를 입력하세요: "))

# 2. 계단 수에 따른 경우의 수 리스트
dp = [0] * (n + 1)

# 3. 초기값
# 계단이 1개일 때
if n >= 1:
    dp[1] = 1

# 계단이 2개일 때는 (1+1), (2)
if n >= 2:
    dp[2] = 2

# 4. 반복문을 이용한 Bottom-up 방식
# i번째 계단에 오르는 법
# = (i-1)번째에서 1칸 올라오는 경우
# + (i-2)번째에서 2칸 올라오는 경우
for i in range(3, n + 1):
    dp[i] = dp[i - 1] + dp[i - 2]

print(f"{n}개의 계단을 오르는 방법의 수는 {dp[n]}가지입니다.")
